        int g = 10,
            q = 1000;
        
        double D = 0,
               M = 0;
        
        String textField1 = jTextField3.getText(), 
               textField2 = jTextField4.getText();
        
        if(checkDouble(textField1) && checkDouble(textField2)) {
            D =  Double.parseDouble(jTextField3.getText());
            M =  Double.parseDouble(jTextField4.getText());
            
            if ((D > 0) && (M > 0)) {
                double Fa = 0,
                        V = 4 * 3.14 * Math.pow((D/2), 3) / 3;
        
                Fa = q*g*V;

                if (Fa >= (M*g))
                    answer.setText("арбуз спелый!");
                else
                    answer.setText("арбуз не спелый");
            } else {
                answer.setText("Введите правильные данные");
            }
            
        } else {
            answer.setText("Введите правильные данные");
        }
